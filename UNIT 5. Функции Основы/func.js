// Function Declaration

function sum1(a, b) {
    return a + b;
}

console.log(sum1(1, 2))


// Function Expression


let sum2 = function(c, d) {
    return c + d;
}

console.log(sum2(2, 2))


// Arrow functions


let sum3 = (e, f) => {
    return e + f
}

console.log(sum3(3, 2))


sum = (a, b) => a + b;

console.log(sum(1, 2))
